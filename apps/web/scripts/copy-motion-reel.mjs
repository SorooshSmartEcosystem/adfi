#!/usr/bin/env node
// Embeds @orb/motion-reel source files as string constants in a
// generated TypeScript module at apps/web/lib/motion-reel-embedded.ts.
// The render route imports that module statically — webpack/nft sees
// the import and bundles the strings into the route's Lambda. At
// runtime the route writes the strings to /tmp/motion-reel/* and
// hands the path to Remotion's bundle().
//
// Why not just copy files: Vercel's per-route file tracer drops any
// file not statically referenced via import/require, even when
// outputFileTracingIncludes lists the path. Strings inside an
// imported .ts module are part of the route's webpack chunk and
// can't be dropped.

import { mkdir, readFile, writeFile, readdir, stat } from "node:fs/promises";
import { existsSync } from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const srcDir = resolve(__dirname, "../../../packages/motion-reel/src");
const destDir = resolve(__dirname, "../lib");
const destFile = resolve(destDir, "motion-reel-embedded.ts");

if (!existsSync(srcDir)) {
  console.error(`[copy-motion-reel] src dir not found: ${srcDir}`);
  process.exit(1);
}

async function walk(dir, base) {
  const out = [];
  const entries = await readdir(dir);
  for (const e of entries) {
    const p = join(dir, e);
    const s = await stat(p);
    if (s.isDirectory()) {
      out.push(...(await walk(p, base)));
    } else if (/\.(tsx?|js|jsx)$/.test(e)) {
      const content = await readFile(p, "utf8");
      out.push({ relativePath: relative(base, p), content });
    }
  }
  return out;
}

const files = await walk(srcDir, srcDir);

// Generate the embedded module. JSON.stringify handles all string
// escaping (backticks, ${, etc.) without a custom escaper.
const banner = `// Auto-generated by apps/web/scripts/copy-motion-reel.mjs.
// DO NOT EDIT — re-run prebuild to regenerate.
// Source: packages/motion-reel/src/

export type EmbeddedFile = { relativePath: string; content: string };

export const MOTION_REEL_FILES: EmbeddedFile[] = ${JSON.stringify(files, null, 2)};
`;

await mkdir(destDir, { recursive: true });
await writeFile(destFile, banner);

console.log(`[copy-motion-reel] embedded ${files.length} files into ${destFile}`);
for (const f of files) {
  console.log(`  ${f.relativePath} (${f.content.length} chars)`);
}
